    private void cargarTabla() {
        modelo.setManga(control.listar());
        tblManga.setModel(modelo);
        tblManga.updateUI();
    }

    private void cargarCombo() {
        try {
            Utilidades.cargarEditorial(cbxEditorial);
            Utilidades.cargarAutor(cbxAutores);
            Utilidades.cargarAtributos(cbxAtributos);
            Utilidades.cargarTipo(cbxTipo);
        } catch (EmptyException | PositionException ex) {
            Logger.getLogger(FrmManga.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void guardarAutor() throws IOException {
        control.guardarAutor(txtAlias.getText());
    }

    private void guardarEditorial() throws IOException {
        control.guardarEditorial(txtNombre.getText(), txtDireccion.getText());
    }

    private void guardarManga() throws EmptyException, PositionException, IOException {
        control.guardarManga(txtTitulo.getText(), Integer.parseInt(txtPaginas.getText()), Integer.parseInt(txtPaginas.getText()),
                cbxAutores.getSelectedItem().toString(), cbxEditorial.getSelectedItem().toString());
    }

