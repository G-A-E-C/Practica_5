    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if (cbxAtributos.getSelectedItem().toString().equals("Paginas")) {
            int pag = Integer.parseInt(txtbuscar.getText());
            try {
                modelo.setManga(control.buscarManga(cbxAtributos.getSelectedItem().toString().toLowerCase(), cbxTipo.getSelectedIndex(), pag));
                tblManga.setModel(modelo);
                tblManga.updateUI();
            } catch (EmptyException | PositionException ex) {
                Logger.getLogger(FrmManga.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            try {
                modelo.setManga(control.buscarManga(cbxAtributos.getSelectedItem().toString().toLowerCase(), cbxTipo.getSelectedIndex(), txtbuscar.getText()));
                tblManga.setModel(modelo);
                tblManga.updateUI();
            } catch (EmptyException | PositionException ex) {
                Logger.getLogger(FrmManga.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

